#include <iostream>
using namespace std;
int main(){
	int num = 15;//Este operador asigna el valor de 15 a num
	num += 5;//Es lo mismo que hacer num=num+5, ahora num vale 20
	num -= 3;//Es lo mismo que hacer num=num-3, ahora num vale 17
	num *= 2;//Es lo mismo que hacer num=num*2, ahora num vale 34
	num /= 4;//Es lo mismo que hacer num=num/4, ahora num vale 8
	num %= 3;//Es lo mismo que hacer num=num%3, ahora num vale 2
}
